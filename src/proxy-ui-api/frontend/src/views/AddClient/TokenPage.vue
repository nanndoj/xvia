<template>
  <div>
    {{$t('wizard.token.info')}}
    <v-text-field
      v-model="search"
      :label="$t('wizard.token.tokenName')"
      single-line
      hide-details
      class="search-input"
      data-test="token-search-input"
    >
      <v-icon slot="append">mdi-magnify</v-icon>
    </v-text-field>

    <v-radio-group v-model="tokenGroup">
      <div class="radio-row" v-for="token in filteredTokens" :key="token.id">
        <v-radio
          :label="`Token ${token.name}`"
          :value="token"
          :disabled="!token.logged_in"
          data-test="token-radio-button"
        ></v-radio>
        <div>
          <large-button
            @click="confirmLogin(token)"
            v-if="!token.logged_in"
            :disabled="!token.available"
            data-test="token-login-button"
          >{{$t('keys.logIn')}}</large-button>
          <large-button
            v-if="token.logged_in"
            outlined
            disabled
            data-test="token-logout-button"
          >{{$t('wizard.token.loggedIn')}}</large-button>
        </div>
      </div>
    </v-radio-group>

    <div class="button-footer">
      <div class="button-group">
        <large-button
          outlined
          @click="cancel"
          :disabled="!disableDone"
          data-test="cancel-button"
        >{{$t('action.cancel')}}</large-button>
      </div>

      <div>
        <large-button
          @click="previous"
          outlined
          class="previous-button"
          data-test="previous-button"
        >{{$t('action.previous')}}</large-button>

        <large-button
          @click="done"
          :disabled="disableNext"
          data-test="next-button"
        >{{$t('action.next')}}</large-button>
      </div>
    </div>
    <TokenLoginDialog :dialog="loginDialog" @cancel="loginDialog = false" @save="tokenLogin" />
  </div>
</template>

<script lang="ts">
import Vue from 'vue';
import { mapGetters } from 'vuex';
import LargeButton from '@/components/ui/LargeButton.vue';
import TokenLoginDialog from '@/components/token/TokenLoginDialog.vue';
import { ValidationProvider, ValidationObserver } from 'vee-validate';
import { Token } from '@/types';

export default Vue.extend({
  components: {
    LargeButton,
    ValidationObserver,
    ValidationProvider,
    TokenLoginDialog,
  },
  computed: {
    ...mapGetters(['tokens']),

    filteredTokens: {
      get(): Token[] {
        return this.$store.getters.tokensFilteredByName(this.search);
      },
    },

    disableSelection() {
      if (this.tokens.length === 1) {
        return true;
      }
      return false;
    },

    disableNext() {
      if (this.tokenGroup) {
        return false;
      }
      return true;
    },
  },
  data() {
    return {
      search: undefined,
      disableDone: true,
      tokenGroup: undefined as Token | undefined,
      loginDialog: false,
    };
  },
  methods: {
    cancel(): void {
      this.$emit('cancel');
    },
    previous(): void {
      this.$emit('previous');
    },
    done(): void {
      if (!this.tokenGroup || !this.tokenGroup.id) {
        return;
      }
      this.$store.dispatch('setCsrTokenId', this.tokenGroup.id);
      this.$emit('done');
    },
    confirmLogin(token: Token): void {
      this.$store.dispatch('setSelectedToken', token);
      this.loginDialog = true;
    },
    tokenLogin(password: string): void {
      this.fetchData();
      this.loginDialog = false;
    },
    fetchData(): void {
      // Fetch tokens from backend
      this.$store
        .dispatch('fetchTokens')
        .then(() => {
          // Preselect the token if there is only one
          if (
            this.filteredTokens.length === 1 &&
            this.filteredTokens[0].logged_in
          ) {
            this.tokenGroup = this.filteredTokens[0];
          }
        })
        .catch((error) => {
          this.$store.dispatch('showError', error);
        });
    },
  },
  created() {
    this.fetchData();
  },
});
</script>

